_SECURITY_SCANNING = """
# ────────────────────────────────────────────
# Security & Compliance
# ────────────────────────────────────────────
sast:
  stage: security
  image: node:20-alpine
  script:
    - npx audit-ci --moderate
    - npx eslint --ext .ts,.tsx src/ --format json --output-file gl-sast-report.json || true
  artifacts:
    reports:
      sast: gl-sast-report.json
    expire_in: 7 days
  allow_failure: true

dependency_scan:
  stage: security
  image: node:20-alpine
  script:
    - npm audit --audit-level=high --json > gl-dependency-report.json || true
  artifacts:
    reports:
      dependency_scanning: gl-dependency-report.json
    expire_in: 7 days
  allow_failure: true

secret_detection:
  stage: security
  image: alpine:latest
  script:
    - apk add --no-cache grep
    - |
      echo "Scanning for hardcoded secrets..."
      PATTERNS="sk-ant-|ghp_|glpat-|AKIA|password=|api_key=|secret="
      if grep -rn -E "$PATTERNS" --include="*.ts" --include="*.tsx" --include="*.js" --include="*.json" . 2>/dev/null | grep -v node_modules | grep -v .env.example; then
        echo "⚠️ Potential secrets found! Review above matches."
        exit 1
      fi
      echo "✅ No hardcoded secrets detected"
  allow_failure: false
"""

_NOTIFICATION_JOB = """
notify_success:
  stage: notify
  image: alpine:latest
  script:
    - echo "✅ Pipeline passed for $CI_PROJECT_NAME"
  when: on_success
  only: [main, tags]

notify_failure:
  stage: notify
  image: alpine:latest
  script:
    - echo "❌ Pipeline FAILED for $CI_PROJECT_NAME — check logs"
  when: on_failure
  only: [main, tags]
"""


