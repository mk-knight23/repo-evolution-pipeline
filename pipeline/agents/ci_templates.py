# CI_TEMPLATES["flutter"] and CI_TEMPLATES["ionic"] have been removed in v2.1


def get_ci_template(framework: str | MobileFramework) -> str:
    """Get the CI/CD template for a given framework."""
    key = framework.value if isinstance(framework, MobileFramework) else framework
    return CI_TEMPLATES.get(key, CI_TEMPLATES["expo"])
