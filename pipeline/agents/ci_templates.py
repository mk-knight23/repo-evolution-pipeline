CI_TEMPLATES: dict[str, str] = {}

CI_TEMPLATES["expo"] = f"""\
# ============================================================================
# 📱 GitLab CI/CD — Expo / React Native (Managed Workflow)
# Generated by Repo Evolution Pipeline v2.0
# ============================================================================

image: node:20

variables:
  EXPO_NO_TELEMETRY: "1"
  CI: "true"
  NODE_OPTIONS: "--max-old-space-size=4096"

stages:
  - install
  - lint
  - test
  - security
  - build
  - deploy
  - notify

cache:
  key: "${{CI_COMMIT_REF_SLUG}}-node"
  paths:
    - node_modules/
    - .expo/
  policy: pull-push

# ────────────────────────────────────────────
# Install
# ────────────────────────────────────────────
install:
  stage: install
  script:
    - npm ci --prefer-offline --no-audit
  artifacts:
    paths: [node_modules/]
    expire_in: 1 hour

# ────────────────────────────────────────────
# Lint & Type Check
# ────────────────────────────────────────────
lint:
  stage: lint
  script:
    - npx tsc --noEmit || echo "Type errors found — review above"
    - npx eslint src/ --ext .ts,.tsx --max-warnings 10 || true
  needs: [install]

# ────────────────────────────────────────────
# Tests
# ────────────────────────────────────────────
test_unit:
  stage: test
  script:
    - npx jest --ci --coverage --passWithNoTests
  coverage: '/All files.*?\\|.*?(\\d+\\.\\d+)/'
  artifacts:
    reports:
      junit: junit.xml
      coverage_report:
        coverage_format: cobertura
        path: coverage/cobertura-coverage.xml
    paths: [coverage/]
    expire_in: 7 days
  needs: [install]

{_SECURITY_SCANNING}

# ────────────────────────────────────────────
# Build — Android
# ────────────────────────────────────────────
build_android:
  stage: build
  script:
    - npx expo export --platform android
  artifacts:
    paths: [dist/]
    expire_in: 30 days
  needs: [test_unit, lint]
  only: [main, tags]

# ────────────────────────────────────────────
# Build — Web (Preview)
# ────────────────────────────────────────────
build_web:
  stage: build
  script:
    - npx expo export --platform web
  artifacts:
    paths: [dist/]
    expire_in: 7 days
  needs: [test_unit]
  except: [tags]

# ────────────────────────────────────────────
# Deploy — EAS (Production Build & Submit)
# ────────────────────────────────────────────
deploy_eas_android:
  stage: deploy
  script:
    - npx eas-cli build --platform android --profile production --non-interactive
    - npx eas-cli submit --platform android --profile production --non-interactive
  only: [tags]
  environment:
    name: production/android

deploy_eas_ios:
  stage: deploy
  script:
    - npx eas-cli build --platform ios --profile production --non-interactive
    - npx eas-cli submit --platform ios --profile production --non-interactive
  only: [tags]
  environment:
    name: production/ios

# ────────────────────────────────────────────
# Deploy — GitLab Pages (web preview)
# ────────────────────────────────────────────
pages:
  stage: deploy
  script:
    - mkdir -p public
    - cp -r dist/* public/ 2>/dev/null || echo "No web export found"
  artifacts:
    paths: [public/]
  needs: [build_web]
  only: [main]
  environment:
    name: preview
    url: https://${{CI_PROJECT_NAMESPACE}}.gitlab.io/${{CI_PROJECT_NAME}}

{_NOTIFICATION_JOB}
"""

